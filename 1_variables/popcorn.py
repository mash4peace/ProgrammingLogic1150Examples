""" Calculate total cost for buying multiple bags of popcorn """

# Create a variable to store cost of one bag of popcorn
popcorn_cost = 4.25
# Create a variable to store number of bags purchased
bags_purchased = 3

# Multiply cost per bag, by total bags purchased, to calculate total
total_cost = popcorn_cost * bags_purchased

# Display total
print(total_cost)